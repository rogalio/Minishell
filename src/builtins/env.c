#include "builtins.h"
#include "libft.h"

void	env(char **envp)
{
  int	i;

  i = 0;
  while (envp[i])
  {
    ft_putstr_fd(envp[i], 1);
    ft_putstr_fd("\n", 1);
    i++;
  }
}